class DB {
    constructor() {
        this.data = DATA_LAKE;
        // this.randomlySortData();
    }

    getData () {
        return this.data;
    }

    randomlySortData() {
        return this.data.sort(() => Math.random() - 0.5);
    }

    getByTag(tag) {
        return this.data.filter(item => item.tag === tag);
    }

    //get all distinct tags
    getAllTags() {
        const tags = this.data.map(item => item.tag);
        const uniqueTags = [...new Set(tags)];
        return uniqueTags;
    }

    getIndexByTag(tag) {
        //array of index 0 throught length of data
        const allIndexes = [];
        this.data.forEach((item, index) => {
            allIndexes.push(index);
        })
        if (tag === "explore"){
            return allIndexes; 
        }
        const indexes = [];
        this.data.forEach((item, index) => {
            if (item.tag === tag) {
                indexes.push(index);
            }
        })
        if (indexes.length === 0) {
            return allIndexes;
        }
        return indexes;
    }
    
    incrementIndex(index) {
        if (index + 1 >= this.data.length) {
            return 0;
        }
        return index + 1;
    }

    decrementIndex(index) {
        if (index - 1 < 0) {
            return this.data.length - 1;
        } 
        return index - 1;
    }

    getByIndex(index) {
        index = parseInt(index);
        return this.data[index];
    }

    getNextData(index) {
        return this.data[this.incrementIndex(index)];
    }

    getPrevData(index) {
        return this.data[this.decrementIndex(index)];
    }

    getTenAboveAndBelow(index) {
        index = parseInt(index);
        const padding = 2;
        const maxIndex = this.data.length;
        let indexes = [];
        for (let i = -padding; i <= padding; i++) {
            let currIndex = index + i;
            if (currIndex < 0) {
                currIndex += maxIndex;
            } else if (currIndex >= maxIndex) {
                currIndex -= maxIndex;
            }
            indexes.push(currIndex);
        }
        return indexes;
    }
}




